package net.argus.emessage.api.ui.bubble.instance;

import java.awt.Color;

public abstract class BubbleColorInstance {

	public abstract Color getUserLightBubble();
	public abstract Color getFriendLightBubble();
	
	public abstract Color getUserDarkBubble();
	public abstract Color getFriendDarkBubble();
	
	
	public abstract Color getUserDarkText();
	public abstract Color getFriendDarkText();
	
	public abstract Color getUserLightText();
	public abstract Color getFriendLightText();
	
	
	public abstract Color getLightName();
	public abstract Color getDarkName();

	
	public abstract Color getLight();
	public abstract Color getDark();
	
}
